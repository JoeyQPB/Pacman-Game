let drawWalls = () => {
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] == 1) {
                // it is wall
                createRect(
                    j * oneBlockSize,
                    i * oneBlockSize,
                    oneBlockSize,
                    oneBlockSize,
                    wallCollor);
            }

            if (j > 0 && map[i][j - 1] == 1) {
                createRect(
                    j * oneBlockSize,
                    i * oneBlockSize + wallOffSet,
                    wallSpaceWith + wallOffSet,
                    wallSpaceWith,
                    wallInnerColor
                )
            }

            if (j < map[0].length - 1 && map[i][j + 1] == 1) {
                createRect(
                    j * oneBlockSize + wallOffSet,
                    i * oneBlockSize + wallOffSet,
                    wallSpaceWith + wallOffSet,
                    wallSpaceWith,
                    wallInnerColor
                )
            }

            if (i > 0 && map[i - 1][j] == 1) {
                createRect(
                    j * oneBlockSize + wallOffSet,
                    i * oneBlockSize,
                    wallSpaceWith,
                    wallSpaceWith + wallOffSet,
                    wallInnerColor
                )
            }

            if (i < map.length - 1 && map[i + 1][j] == 1) {
                createRect(
                    j * oneBlockSize + wallOffSet,
                    i * oneBlockSize + wallOffSet,
                    wallSpaceWith,
                    wallSpaceWith + wallOffSet,
                    wallInnerColor
                )
            }
        }
    }
}

